<h2 class="maintitle">Wimborne Laugh Out Loud 2017</h2>

<div class="fb-share-button" data-type="button_count" data-size="large" style="margin:0;"></div>

<p>Are you funny? Do you want a chance to prove it - on stage, in front of a warm, supportive audience? If your answer is a great big "YES!" then our "<strong>Laugh Out Loud</strong>" New Act Competition is just right for you.</p>
	
	<p>This year Wimborne Minster Folk Festival introduces comedy into its program with "Laugh Out Loud", its first ever New Act Comedy Competition, culminating in the final on Friday 9 June - at which the finalists will appear on the same bill as surreal word wizard Noel James among other superb acts ("<i>Noel James has a razor-sharp wit that belies his chaotic character</i>" - Chortle).</p>
	
	<h2 class="subtitle">Want to Know More?</h2>
	
	<p>With cash prizes and an appearance at Wimborne Comedy Club on offer there's lots to aim for.</p>
	
	<p>Successful applicants will be invited to one of two heats to be held in Wimborne (BH21).
There will be a maximum of 12 entrants in each heat - only 24 people will be accepted into the competition (we will run a reserve list in case of drop outs and aim to expand entry next year).</p>
	
	<p>The top 3 from each heat will go forward into the final.</p>
	
	<ul>
	<li>Heat 1 is to be held at 8pm in The Man In The Wall, 10 West Borough, Wimborne Minster BH21 1NF on Monday 24 April.</li>
    <li>Heat 2 is to be held at 8pm in Queen Elizabeth's School, Blandford Road, Wimborne Minster BH21 4D on Saturday 6 May.</li>
	<li>The Final will be held on Friday 9 June at Queen Elizabeth's School.</li>
	</ul>
	
	<p>During the heats each slot will be 8 minutes long. If you make it to the final you'll have an extra two minutes to impress the judges and audience (10 minutes maximum).</p>
	
	<p><strong>Audience entry to the heats will be on a "Pay what you want" basis</strong> - that is, free to enter but with a bucket collection at the end into which audience members can give a donation reflecting the level of laughter that they enjoyed on the night.</p>
	
	<p><strong>Audience entry at the final will be by ticket</strong> (available in advance or on the door if available, advance tickets available online on our <a href="/tickets" rel="bookmark"><strong>Tickets Page</strong></a>). The ticket price will be just &#163;5 or included in Festival Weekend/Day Passes, a great value price for a comedy night featuring established comedians as well as the New Act Competition finalists. So, no reason not to bring your family and friends!
<br />All money collected at the heats or raised from tickets sales will go towards the running costs of the competition and of course the Wimborne Minster Folk Festival.
<br />There are no fees or expenses payable to competition entrants, except for the cash prizes.</p>
	
	<p><strong>Judging of the heats</strong> will be done by a mixture of audience voting and judges scoring. Heat results will be announced on the night and those going through to the final will be emailed for confirmation.
		<br /><strong>Judging of the final</strong> will be done solely by a panel of judges, including comedian Noel James and local comedy promoter Richard Jay.
<br />All material should be original and the creation of the entrant. Plagiarism will be penalised.</p>
	
	<p><strong>What can we win?</strong>
<br />As well as the glory of taking part in the first ever "Laugh Out Loud" New Act Comedy Competition, the winner of the final will receive &#163;75 and a 10 minute slot alongside professional comedians at Wimborne Comedy Club (for which expenses up to &#163;30 will be available).
		<br />Prizes of &#163;50 and &#163;25 will be awarded to the finalists placed 2nd and 3rd respectively.</p>
	
	<h2 class="subtitle">Are you eligible?</h2>
	<p>To find out if you are eligible, how to apply and to view competition terms, please continue reading at <a href="http://wimbornecomedy.co.uk/lolwimborne" rel="tag"><strong>WimborneComedy.co.uk/lolwimborne</strong></a>, but hurry as <strong>entries must arrive by the end of Sunday 16 April 2017 (that's not long!)</strong></p>



