<h2 class="maintitle">Wimborne Live & Loud 2017</h2>

<div class="fb-share-button" data-type="button_count" data-size="large" style="margin:0;"></div>

<p>Wimborne Minster Folk Festival is proud to present the return of the annual youth music competition Live & loud and this year it's packed with new features!</p>

<p>The highly anticipated event will now be running from the end of April through to the end of May, with rounds of auditions. Those applying will get a choice of audition locations, all taking place in Wimborne and then will be selected to join us at a semi final round and onwards to our prestigious grand final taking place at the Allendale Centre on the 26 May 2017 where we will crown the winner.</p>

<p>Prizes will include a place on the Youth Music Stage on Willow Walk during the festival weekend, the chance to record a single, songwriting workshop and vouchers! So, if you're a soloist, an acoustic duo or a full band from the age of 13 to 26 then we would love to see what you can do. Enter your details bellow with a link of you performing and we'll get you signed up for an audition date.</p>
	
	<h2 class="subtitle">Semi Finalists</h2>
	<p>Square Records (23 April): Lucy Beardsley and Lee Rasdall-Dove
	<br />The White Hart (28 April): One Word and Hello Hawaii</p>
	
	<p>Each can be seen for the semi-finals at The Olive Branch, Wimborne on 12 May, which will be free entry!</p>

<h2 class="maintitle"><a href="/apply/live-2017" rel="bookmark">Apply Online</a></h2>

<p>For your chance to win the awesome prizes below, <a href="/apply/live-2017" rel="bookmark"><strong>Apply Online</strong></a> today!</p>

<h2 class="subtitle">The Prizes</h2>
<p>The winning act will be programmed into the festival line-up on the weekend of 9, 10, 11 June 2017 (save the dates!) on the Willow Walk Youth Music Stage and will also win a recording session to record your own single!</p>

<p>Second place will also win a slot on stage at the festival as well as a songwriting workshop session with <a href="/?event=8" rel="bookmark">Polly Morris</a>.</p>

<p>Third place will win music vouchers.</p>



